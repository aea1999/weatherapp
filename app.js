//selectors
const form = document.querySelector("form");
const input = document.querySelector("#search");
const state = document.querySelector("#state");
const tempa = document.querySelector("#temp");
const darkMode = document.querySelector("#darkmode");
//event listener
form.addEventListener("submit", fetchCity);
darkMode.addEventListener("click", toggleDarkMode);

//getting the city's url and the data
function fetchCity(e) {
  e.preventDefault();
  let url = `https://api.openweathermap.org/data/2.5/weather?q=${input.value}&units=metric&appid=daab9daf860be8b94be85e10bc93b4dc
`;
  fetch(url)
    .then(checkStatusAndParse)
    .then(getData)
    .catch((err) => console.log(err));
  this.reset();
}
//checking if the input is correct, and parsing to json
function checkStatusAndParse(response) {
  if (!response.ok) throw new Error(`Status Code Error:${response.status}`);
  return response.json();
}

// get the data and changing the crospending details in the page
function getData(data) {
  tempa.textContent = data.main.temp;
  display(data.weather[0].main);
}

//display the weather state by using a switch statement and putting the svg
function display(weather) {
  switch (weather) {
    case "Rain":
      state.innerHTML = rain;
      break;
    case "Clouds":
      state.innerHTML = clouds;
      break;
    case "Clear":
      state.innerHTML = sunny;
      break;
    case "Snow":
      state.innerHTML = snow;
  }
}

//changing all the elements to darkmode
function toggleDarkMode(e) {
  document.body.classList.toggle("dark");
  state.classList.toggle("dark");
  tempa.classList.toggle("dark");
  darkMode.classList.toggle("dark");
}
